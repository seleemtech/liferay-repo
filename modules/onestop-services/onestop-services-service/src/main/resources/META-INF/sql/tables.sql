create table HROS_NewsEntry (
	NEWS_ENTRY_ID LONG not null primary key,
	GROUP_ID LONG,
	COMPANY_ID LONG,
	USER_ID LONG,
	CREATE_DATE DATE null,
	MODIFIED_DATE DATE null,
	NEWS_TITLE VARCHAR(75) null,
	NEWS_SUMMARY VARCHAR(75) null,
	NEWS_CONTENT VARCHAR(75) null,
	NEWS_IMAGE_ID LONG,
	NEWS_SCOPE_ID LONG,
	ARCHIVE BOOLEAN,
	ALLOWED_IN_DASHBOARD BOOLEAN,
	EXPIRE_DATE DATE null,
	TO_NEWS_EMAIL VARCHAR(75) null,
	STATUS INTEGER,
	STATUS_BY_USER_ID LONG,
	STATUS_BY_USER_NAME VARCHAR(75) null,
	STATUS_DATE DATE null
);